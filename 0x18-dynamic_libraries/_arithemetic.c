#include <stdio.h>

/**
 * div - function to divide
 * @x: variable
 * @y: variable
 *
 * Return result
 */
int div(int x, int y)
{
	return (x / y);
}

/**
 * add - to add
 * @x: variable
 * @y: variable
 * Return: add
 */
int add(int x, int y)
{
	return (x + y);
}

/**
 * sub - to sub
 * @x: variable
 * @y: variable
 * Return: sub
 */
int sub(int x, int y)
{
	return (x - y);
}

/**
 * mul - to mul
 * @x: variable
 * @y: variable
 * Return:  mul
 */
int mul(int x, int y)
{
	return (x * y);
}
